function openMenuSection1() {
    document.getElementById("headers-adaptive").style.display = "flex";
    document.getElementById("nav-adapt").style.display = "none";
    document.getElementById("btn-adapt").style.display = "none";
}

function closeMenuSection1() {
    document.getElementById("headers-adaptive").style.display = "none";
    document.getElementById("nav-adapt").style.display = "block";
    document.getElementById("btn-adapt").style.display = "block";
}